import requests
import json
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from datetime import datetime, timedelta
import pytz
from IPython.display import display, HTML
import warnings
warnings.filterwarnings('ignore')

print("🚀 Google Colab Tidal Analysis SR01 SUNGSANG siap!")

# --- 1. DOWNLOAD DATA DARI THINGSPEAK (Field 2, 8000 latest entries)
print("\n📥 Downloading data dari ThingSpeak...")
url = "https://api.thingspeak.com/channels/3097834/feeds.json?results=8000"
try:
    response = requests.get(url, timeout=30)
    response.raise_for_status()
    data = response.json()
    feeds = data['feeds']
    print(f"✅ Berhasil download {len(feeds)} entries")
except Exception as e:
    print(f"❌ Error: {e}")
    print("Coba manual download JSON dan upload ke Colab")
    raise

# --- 2. BUAT DATAFRAME
print("\n🔄 Processing data...")
df_list = []
jakarta_tz = pytz.timezone('Asia/Jakarta')

for f in feeds:
    if f['field2'] is not None:
        dt = pd.to_datetime(f['created_at']).tz_convert(jakarta_tz)
        df_list.append({
            'datetime': dt,
            'entry_id': int(f['entry_id']),
            'field2': float(f['field2'])
        })

df = pd.DataFrame(df_list).sort_values('datetime').reset_index(drop=True)
print(f"✅ Dataframe: {len(df)} points setelah filter NA")

# --- 3. FILTER MULAI ENTRY_ID = 1859
start_id = 1859
tmp = df[df['entry_id'] >= start_id][['datetime', 'entry_id', 'field2']].reset_index(drop=True)
n = len(tmp)
print(f"✅ Filtered: {n} points mulai entry_id {start_id}")

# --- 4. BANGUN ULANG DATETIME INTERVAL 10 MENIT
start_time_tag = pd.Timestamp("2026-03-01 01:46:02", tz='Asia/Jakarta')
datetime_seq = pd.date_range(start=start_time_tag, freq='10T', periods=n)
# TAMBAH KOLOM field2_neg & hms
extract_data = tmp.copy()
extract_data['datetime'] = datetime_seq
extract_data['hms'] = extract_data['datetime'].dt.strftime('%H:%M:%S')
extract_data['field2_neg'] = extract_data['field2'] * -1

print(f"✅ Rentang waktu: {extract_data['datetime'].min()} → {extract_data['datetime'].max()}")

# --- 5. TAMPILKAN DATA SAMPLE
print("\n📊 SAMPLE DATA (10 pertama):")
display(extract_data[['entry_id', 'datetime', 'hms', 'field2', 'field2_neg']].head(10))

print("\n⏰ HMS INTERVAL 10 MENIT (10 pertama):")
print(extract_data['hms'].head(10).tolist())

# --- 6. ANALISIS INTERVAL
diff_min = pd.Series(np.insert(pd.Series(np.diff(extract_data['datetime'])).dt.total_seconds()/60, 0, np.nan))
extract_data['diff_min'] = diff_min.round(1)
print("\n📈 RINGKASAN INTERVAL (menit):")
print(diff_min.describe())

# --- 7. SIMPAN CSV 1
extract_data.to_csv("field2_tidal_entry1859_2026.csv", index=False)
print("\n💾 CSV 1: field2_tidal_entry1859_2026.csv ✅")

# --- 8. HITUNG DEVIASI
print("\n📊 STATISTIK DEVIASI:")
mean_field2 = extract_data['field2'].mean()
extract_data['field2_deviasi'] = extract_data['field2'] - mean_field2

mean_field2_neg = extract_data['field2_neg'].mean()
extract_data['field2_neg_deviasi'] = extract_data['field2_neg'] - mean_field2_neg

print(f"Field2     Mean: {mean_field2:.3f}, Deviasi range: {extract_data['field2_deviasi'].min():.3f} → {extract_data['field2_deviasi'].max():.3f}")
print(f"Field2_neg Mean: {mean_field2_neg:.3f}")

print("\n📈 SUMMARY DEVIASI:")
print("Field2_deviasi:", extract_data['field2_deviasi'].describe())
print("Field2_neg_deviasi:", extract_data['field2_neg_deviasi'].describe())

# --- 9. SIMPAN CSV 2 (LENGKAP)
extract_data.to_csv("field2_tidal_entry1859_deviasi_2026.csv", index=False)
print("💾 CSV 2: field2_tidal_entry1859_deviasi_2026.csv ✅")

