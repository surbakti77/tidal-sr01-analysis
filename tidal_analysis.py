def plot_tidal(ax, data, ycol, title, ylabel, color, mean_line, mean_val):
    ax.plot(data['datetime'], data[ycol], color=color, linewidth=2)
    ax.axhline(mean_line, color='blue', linewidth=3, linestyle='--', alpha=0.8)
    ax.text(0.72, 0.95, f'Mean = {mean_val:.3f}', transform=ax.transAxes,
            color='blue', fontsize=11, bbox=dict(boxstyle='round', facecolor='white', alpha=0.8))
    ax.set_title(title, fontsize=14, pad=20)
    ax.set_xlabel('Waktu (WIB)', fontsize=12)
    ax.set_ylabel(ylabel, fontsize=12)
    ax.grid(True, alpha=0.3)
    ax.tick_params(axis='x', rotation=45)

# PLOT 1: FIELD2 ASLI (Jarak dari Alat)
fig1, ax1 = plt.subplots(figsize=(14, 8))
plot_tidal(ax1, extract_data, 'field2', 'Kurva Pasang Surut (Jarak dari Alat Ukur)',
           'Jarak dari Alat (m)', 'red', mean_field2, mean_field2)
plt.tight_layout()
plt.savefig("plot1_fielddata_dari_alat.png", dpi=150, bbox_inches='tight')
plt.show()
print("🖼️  PLOT 1: plot1_fielddata_dari_alat.png ✅")

# PLOT 2: FIELD2_NEG (Tinggi Muka Air)
fig2, ax2 = plt.subplots(figsize=(14, 8))
plot_tidal(ax2, extract_data, 'field2_neg', 'Kurva Pasang Surut (Tinggi Muka Air)',
           'Tinggi Muka Air (m)', 'red', mean_field2_neg, mean_field2_neg)
plt.tight_layout()
plt.savefig("plot2_field2_neg_entry1859.png", dpi=150, bbox_inches='tight')
plt.show()
print("🖼️  PLOT 2: plot2_field2_neg_entry1859.png ✅")

# PLOT 3: DEVIASI FIELD2 ASLI
fig3, ax3 = plt.subplots(figsize=(14, 8))
plot_tidal(ax3, extract_data, 'field2_deviasi', 'Anomali Jarak dari Alat',
           'Deviasi dari Mean (m)', 'darkgreen', 0, 0)
plt.tight_layout()
plt.savefig("plot3_field2_deviasi_entry1859.png", dpi=150, bbox_inches='tight')
plt.show()
print("🖼️  PLOT 3: plot3_field2_deviasi_entry1859.png ✅")

# PLOT 4: DEVIASI FIELD2_NEG
fig4, ax4 = plt.subplots(figsize=(14, 8))
plot_tidal(ax4, extract_data, 'field2_neg_deviasi', 'Anomali Pasut Real',
           'Deviasi dari Mean (m)', 'darkorange', 0, 0)
ax4.legend(['Deviasi', 'Mean=0'], loc='upper right', fontsize=11)
plt.tight_layout()
